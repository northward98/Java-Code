package variable;

public class Join {
    public static void main(String[] args) {
        //字符串拼接
        String s1 = "三国演义";
        String s2 = "西游记";
        System.out.println(s1 + s2);//三国演义西游记
        //字符拼接
        char c1 = 'a';
        char c2 = 'b';
        System.out.println(c1 + c2);//97+98
    }
}